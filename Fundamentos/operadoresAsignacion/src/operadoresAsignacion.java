
public class operadoresAsignacion {
    public static void main(String[] args) {
        int a=3,b=2;
        var resultado = a +5 -b ;
        System.out.println("resultado = " + resultado);
        a +=1;
        System.out.println("a = " + a);
        a +=3;
        System.out.println("a = " + a);
        a -=2;
        System.out.println("a = " + a);
        // *= /= %=
        a *=2;
        System.out.println("a = " + a);
        a /=3;
        System.out.println("a = " + a);
        a %=2;
        System.out.println("a = " + a);
        
    }
    
}
